def normalize_language(language: str | None) -> str:
    if language in TEXTS:
        return language
    return "uz"


def get_text(language: str | None, key: str, **kwargs: str) -> str:
    lang = normalize_language(language)
    return TEXTS[lang][key].format(**kwargs)


def get_menu_text(language: str | None, key: str) -> str:
    lang = normalize_language(language)
    return MENU_TEXTS[lang][key]
